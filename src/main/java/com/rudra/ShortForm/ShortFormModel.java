package com.rudra.ShortForm;

public class ShortFormModel {
    private Integer id;
    private String sf;
    private String ff;

    public Integer getId() {
        return id;
    }

    public String getSf() {
        return sf;
    }

    public String getFf() {
        return ff;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public void setSf(String sf) {
        this.sf = sf;
    }

    public void setFf(String ff) {
        this.ff = ff;
    }
}
